#!/usr/bin/env bash
# -----------------------------------------------------------------------------
# krusader-language.sh <iso-code|system>
# -----------------------------------------------------------------------------
# Activates the requested UI language for Krusader / Kate / KDE.
# Called by the cont-init firstrun hook on every container start so the user
# can switch via the Unraid template variable KRUSADER_LANG without touching
# the appdata folder.
#
# Mechanism:
#   * Set kdeglobals[Translations].Language=<code>
#   * Export LANG / LANGUAGE / LC_ALL via /config/.profile.d snippet
#     (KasmVNC's startwm.sh sources /etc/profile.d → which sources us)
#   * Persist a hint file for the s6-overlay env layer
# -----------------------------------------------------------------------------
set -e

CODE="${1:-de}"
CONFIG_HOME="/config/.config"
PROFILE_D="/config/.profile.d"
ENV_FILE="${PROFILE_D}/krusader-lang.sh"

log() { echo "[krusader-language] $*"; }

mkdir -p "${CONFIG_HOME}" "${PROFILE_D}"

# Map ISO-Code → (Locale, Language-Chain, KDE-Translation-Code)
case "${CODE}" in
    de|de_DE|de_DE.UTF-8)
        LOCALE="de_DE.UTF-8"; CHAIN="de_DE:de:en"; KDE="de";;
    en|en_US|en_US.UTF-8)
        LOCALE="en_US.UTF-8"; CHAIN="en_US:en";    KDE="en_US";;
    fr|fr_FR|fr_FR.UTF-8)
        LOCALE="fr_FR.UTF-8"; CHAIN="fr_FR:fr:en"; KDE="fr";;
    es|es_ES|es_ES.UTF-8)
        LOCALE="es_ES.UTF-8"; CHAIN="es_ES:es:en"; KDE="es";;
    it|it_IT|it_IT.UTF-8)
        LOCALE="it_IT.UTF-8"; CHAIN="it_IT:it:en"; KDE="it";;
    nl|nl_NL|nl_NL.UTF-8)
        LOCALE="nl_NL.UTF-8"; CHAIN="nl_NL:nl:en"; KDE="nl";;
    pl|pl_PL|pl_PL.UTF-8)
        LOCALE="pl_PL.UTF-8"; CHAIN="pl_PL:pl:en"; KDE="pl";;
    pt|pt_PT|pt_PT.UTF-8)
        LOCALE="pt_PT.UTF-8"; CHAIN="pt_PT:pt:en"; KDE="pt";;
    ru|ru_RU|ru_RU.UTF-8)
        LOCALE="ru_RU.UTF-8"; CHAIN="ru_RU:ru:en"; KDE="ru";;
    ja|ja_JP|ja_JP.UTF-8)
        LOCALE="ja_JP.UTF-8"; CHAIN="ja_JP:ja:en"; KDE="ja";;
    zh|zh_CN|zh_CN.UTF-8)
        LOCALE="zh_CN.UTF-8"; CHAIN="zh_CN:zh:en"; KDE="zh_CN";;
    tr|tr_TR|tr_TR.UTF-8)
        LOCALE="tr_TR.UTF-8"; CHAIN="tr_TR:tr:en"; KDE="tr";;
    cs|cs_CZ|cs_CZ.UTF-8)
        LOCALE="cs_CZ.UTF-8"; CHAIN="cs_CZ:cs:en"; KDE="cs";;
    system|"")
        LOCALE=""; CHAIN=""; KDE="";;
    *)
        # Best-effort fallback for any other ISO code
        LOCALE="${CODE}.UTF-8"; CHAIN="${CODE}:en"; KDE="${CODE}";;
esac

# -- system / unset ----------------------------------------------------------
if [[ -z "${LOCALE}" ]]; then
    log "KRUSADER_LANG=system – removing forced language."
    rm -f "${ENV_FILE}"
    if [[ -f "${CONFIG_HOME}/kdeglobals" ]]; then
        # Remove [Translations] block (best-effort)
        sed -i '/^\[Translations\]/,/^\[/{/^Language=/d}' "${CONFIG_HOME}/kdeglobals" || true
    fi
    exit 0
fi

log "Activating language: ${LOCALE} (KDE=${KDE})"

# -- 1) Profile snippet (sourced by interactive shells & dbus session) --------
cat > "${ENV_FILE}" <<EOF
# Auto-generated by krusader-language.sh – do not edit by hand.
export LANG=${LOCALE}
export LANGUAGE=${CHAIN}
export LC_ALL=${LOCALE}
EOF
chmod 644 "${ENV_FILE}"

# -- 2) Update kdeglobals [Translations] section ------------------------------
if [[ -f "${CONFIG_HOME}/kdeglobals" ]]; then
    if grep -q '^\[Translations\]' "${CONFIG_HOME}/kdeglobals"; then
        sed -i '/^\[Translations\]/,/^\[/{/^Language=/d}' "${CONFIG_HOME}/kdeglobals"
        sed -i "/^\[Translations\]/a Language=${KDE}"     "${CONFIG_HOME}/kdeglobals"
    else
        printf "\n[Translations]\nLanguage=%s\n" "${KDE}" >> "${CONFIG_HOME}/kdeglobals"
    fi
else
    cat > "${CONFIG_HOME}/kdeglobals" <<EOF
[Translations]
Language=${KDE}
EOF
fi

# -- 3) Make sure the autostart script picks up the env -----------------------
# The KasmVNC base image sources /etc/profile.d/* before running /defaults/autostart.
# We drop a system-wide snippet symlinked from our user-config so resets are easy.
ln -sf "${ENV_FILE}" /etc/profile.d/zz-krusader-lang.sh 2>/dev/null || true

log "Language set – LANG=${LOCALE} LANGUAGE=${CHAIN}"
exit 0
